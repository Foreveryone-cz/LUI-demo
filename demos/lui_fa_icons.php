<h1>FontAwesome Icons</h1>
<section class="item">
	<h3>.fa_b_[icon-name] | .fa_a_[icon-name]</h3>
	<div class="content">
		<p>Using FontAwesome icons for :before/:after: <strong>Works only with pair elements. Wont work with input, use button instead</strong></p>

<pre data-title="Usage:">
class="<span class="highlight">fa_b_eye</span>"
</pre>

		<p>
			<a class="fa_b_glass">icon before</a>
		</p>

<pre data-title="Usage:">
class="<span class="highlight">fa_a_eye</span>"
</pre>
		<p>
			<a class="fa_a_eye">icon after</a>
		</p>
		
		<p>
			List of available icons can be found on FontAwesome official website: <a href="//fontawesome.io/icons/" target="_blank"><b>fontawesome.io/icons/</b></a>
		</p>
		<p>
			Usage is simple: fa_b_<b>name</b> or  fa_a_<b>name</b>
		</p>
	</div>
</section>